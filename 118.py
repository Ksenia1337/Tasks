user_input = input("Введите строку: ")
слова = []
текущее_слово = ""

for символ in user_input:
    if 'a' <= символ <= 'z' or 'A' <= символ <= 'Z' or '0' <= символ <= '9' or символ == "'" or символ == '-':
        текущее_слово += символ
    elif текущее_слово:
        слова.append(текущее_слово)
        текущее_слово = ""

if текущее_слово:
    слова.append(текущее_слово)

print(слова)